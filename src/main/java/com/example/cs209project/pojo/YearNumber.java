package com.example.cs209project.pojo;

public class YearNumber {
    String year;
    int numAWT;
    int numSwing;
    int numJavaFx;
    int numSWT;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public int getNumAWT() {
        return numAWT;
    }

    public void setNumAWT(int numAWT) {
        this.numAWT = numAWT;
    }

    public int getNumSwing() {
        return numSwing;
    }

    public void setNumSwing(int numSwing) {
        this.numSwing = numSwing;
    }

    public int getNumJavaFx() {
        return numJavaFx;
    }

    public void setNumJavaFx(int numJavaFx) {
        this.numJavaFx = numJavaFx;
    }

    public int getNumSWT() {
        return numSWT;
    }

    public void setNumSWT(int numSWT) {
        this.numSWT = numSWT;
    }
}
